#Operadores logicos  de and, or y not

#and

x = True
y = False

resultado = x and y
print(resultado) #Esto imprimira false, ya que ambas condiciones no son verdaderas


#or

x = True
y = False

resultado = x or y
print(resultado) #Esto imprimira True, ya que al menos una condición es verdadera

#not 

x = True

resultado = not x
print(resultado) #Esto imprimirá Flase, ya que la expresión original es Verdadera

