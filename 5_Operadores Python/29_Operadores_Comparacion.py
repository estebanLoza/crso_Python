#OPERADORES DE COMPRACION NOS PERMITEN SABER SI DOS VALORES SON IGUALES O DISTINTOS
#Regresandos con una respuesta booleana (TRUE o FALSE)



# PARENTECIS ES OPCIONAL

a = 4
b = 6

resultado = a == b
print(f'Resultado == : {resultado}')

resultado = a != b
print(f'Resultado != : {resultado}')

resultado = a > b
print(f'Resultado > : {resultado}')

resultado = a < b
print(f'Resultado < : {resultado}')

resultado = a >= b
print(f'Resultado >= : {resultado}')

resultado = a <= b
print(f'Resultado <= : {resultado}')