"""
Pedir al usuario 2 numeros, sumarlos, restarlos, multiplicarlos y dividirlos

"""

num1 = float(input("Ingrese el primer numero: "))

num2 = float(input("Ingrese el segundo numero: "))


suma = num1 + num2

resta = num1 - num2

multi = num1 * num2

division = num1 / num2


print(f"El resultado de la suma es de {suma}")
print(f"El resultado de la resta es de {resta}")
print(f"El resultado de la multi es de {multi}")
print(f"El resultado de la division es de {division}")


